C UTEP Electronic Structure Lab (2020)
      SUBROUTINE PAMLMSIC(MODE,LSPN,LFM,MPTS,WMSA,SICP,RPTS)
! LFM: change (orbital index -> total number of orbitals)
! SICP: change ( 1d NMAX -> 2d NMAXxMAX_OCC )
#ifdef MPI
      use mpidat1,only : NPROC,NCALLED,IRANK
      use SICMAT,only : SIC,SIC_COL,ZMGGAS
      use FRM,only    : BFRM,RESULTS,LFRM,DEBDAX
      USE XTMP2A,ONLY : ISMGGA
      use global_inputs,only : libxc1
! Conversion to implicit none.  Raja Zope Thu Aug 17 14:34:56 MDT 2017

!     INCLUDE  'PARAMAS'  
      INCLUDE  'PARAMA2'  
      INCLUDE 'mpif.h'
      INCLUDE 'commons.inc' 
      INTEGER :: IERR,IPROC,IWF,JOB,JWF,LFM,MPTS,NMAX,LSPN
      REAL*8 :: BUF,ADD0,ADD1,ADD2,ADD3,RPTS,SICP,WMSA
      PARAMETER (NMAX=MPBLOCK) 
      INTEGER TID,ITRANS(3),MODE,TAG
      INTEGER IRECVSTAT(MPI_STATUS_SIZE)
      DIMENSION WMSA(NMAX),SICP(NMAX,MAX_OCC),RPTS(3,NMAX)
      DIMENSION ADD1(NMAX),ADD2(NMAX),ADD3(3,NMAX),ADD0(3)
!     DIMENSION BUF(MAX_OCC,MAX_OCC)
CCC PUT ALL OF SICLGMATs specific common blocks here.
C     COMMON/MIXPOT/POTIN(MAX_PTS*MXSPN),POT(MAX_PTS*MXSPN)
C     COMMON/TMP1/COULOMB(MAX_PTS),RHOG(MAX_PTS,10,MXSPN)
C     COMMON/TMP2/PSIG(NMAX,10,MAX_OCC)
C    & ,PTS(NSPEED,3),GRAD(NSPEED,10,6,MAX_CON,3)
C    & ,RVECA(3,MX_GRP),ICOUNT(MAX_CON,3)
!      COMMON/SICMAT/SIC(MAX_OCC,MAX_OCC,MXSPN)
!      COMMON/FRM/BFRM(3,MAX_OCC,MXSPN),RESULTS(13, MAX_OCC,MXSPN),
!     &  LFRM(MXSPN),DEBDAX(3,MAX_OCC,MXSPN)

      IF (IRANK.NE.0) THEN
        PRINT *,'FATAL: PAMLMSIC CALLED BY WORKER'
        CALL STOPIT
      END IF
C     PRINT*,'IN PAMLSIC MODE=',MODE
      IF(MODE.EQ.1)THEN
        NCALLED=NCALLED+1
        CALL GTNTID(TID)
C       PRINT*,'WORKER:',TID,' IS ABOUT TO BE CALLED'
        JOB=34 ! CHECK THIS
        TAG=0  ! I AM THE MANAGER (0)
        CALL MPI_SSEND(JOB,1,MPI_INTEGER,TID,TAG,MPI_COMM_WORLD,IERR)

        ITRANS(1)=LSPN
        ITRANS(2)=LFM
        ITRANS(3)=MPTS
        TAG=2401
C       PRINT*,IRANK,'PAM LFM IS ZERO:',LFM
        CALL MPI_SSEND(ITRANS(1),3,MPI_INTEGER,
     &                 TID,TAG,MPI_COMM_WORLD,IERR)
        TAG=2402
        CALL MPI_SSEND(WMSA(1),NMAX,MPI_DOUBLE_PRECISION,
     &                 TID,TAG,MPI_COMM_WORLD,IERR)
        TAG=2403
        CALL MPI_SSEND(SICP(1,1),NMAX*MAX_OCC,MPI_DOUBLE_PRECISION,
     &                 TID,TAG,MPI_COMM_WORLD,IERR)
        TAG=2404
        CALL MPI_SSEND(RPTS(1,1),3*NMAX,MPI_DOUBLE_PRECISION,
     &                 TID,TAG,MPI_COMM_WORLD,IERR)
        TAG=2405
        CALL MPI_SSEND(ISMGGA,1,MPI_LOGICAL,
     &                 TID,TAG,MPI_COMM_WORLD,IERR)
        TAG=2406
        CALL MPI_SSEND(LIBXC1,1,MPI_LOGICAL,
     &                 TID,TAG,MPI_COMM_WORLD,IERR)
        IF(ISMGGA.OR.LIBXC1)THEN
         TAG=2407
         CALL MPI_SSEND(ZMGGAS,4*NMAX*MAX_OCC,MPI_DOUBLE_PRECISION,
     &                 TID,TAG,MPI_COMM_WORLD,IERR)
        ENDIF
C       PRINT*,'I AM RETURNING TO FRMORB',IRANK
        IF(NCALLED.EQ.NPROC)THEN
C         PRINT*,'WAITING FOR SOMEONE TO FINISH'
          CALL CKWORKER(4,TID)
C         PRINT*,'DONE WAITING'
        END IF  
      END IF
c=============================================
C     PRINT*,'MODE:',MODE
      IF (MODE .EQ. 2 ) THEN
C     PRINT*,'NCALLED:',NCALLED,'SHOULD BE ZERO'
        IF(NCALLED.NE.0)CALL STOPIT
        IF(LFM.EQ.0)THEN
          PRINT*,'LFM IS ZERO:',LFM
          CALL STOPIT
        END IF
        JOB=35 ! CHECK THIS
        TAG=0  ! I AM THE MANAGER (0)
        DO IPROC=1,NPROC
C        PRINT*,'IPROC',IPROC
         CALL MPI_SSEND(JOB,1,MPI_INTEGER,IPROC,TAG,MPI_COMM_WORLD,IERR)
        END DO
!       DO IWF=1,MAX_OCC
!        DO JWF=1,MAX_OCC
!         SIC(JWF,IWF,LSPN)= 0.0D0
!        END DO 
!       END DO
!       CALL MPI_BARRIER(MPI_COMM_WORLD,IERR)
        CALL MPI_REDUCE(MPI_IN_PLACE,SIC(1,1,LSPN),MAX_OCC*MAX_OCC,
     &                   MPI_DOUBLE_PRECISION,MPI_SUM,0,
     &                   MPI_COMM_WORLD,IERR)
!       CALL MPI_REDUCE(MPI_IN_PLACE,SIC_COL(1),MAX_OCC,
!    &                   MPI_DOUBLE_PRECISION,MPI_SUM,0,
!    &                   MPI_COMM_WORLD,IERR)
!       DO IWF=1,MAX_OCC
!        DO JWF=1,MAX_OCC
!         SIC(JWF,IWF,LSPN)= BUF(JWF,IWF)
!        END DO       
!       END DO
      END IF
#endif
      RETURN
      END
