      SUBROUTINE PASLMSIC_GROUP(MODE)
#ifdef MPI
      use debug1
      use mpidat1, only : IRANK,SHMRANK,SHMCOMM
      use SICMAT, only : SIC_COL
      use FRM, only : BFRM,RESULTS,LFRM
      USE XTMP2A,ONLY : ISMGGA,MIXINS
      use global_inputs,only : libxc1
! Conversion to implicit none.  Raja Zope Thu Aug 17 14:34:56 MDT 2017

!     INCLUDE  'PARAMAS'  
      INCLUDE  'PARAMA2'  
      INCLUDE 'mpif.h'
!     INCLUDE 'commons.inc'
      save
      INTEGER :: IERR,JWF,LFM,LFN,LSPN,MISTAKES,MPTS,NMAX
      REAL*8 :: APT1,APT2,BUF,BUF0,BUF1,BUF2,BUF3,RPTS,SICP,WMSA
      PARAMETER (NMAX=MPBLOCK)
      INTEGER TID,ITRANS(3),MODE,TAG
      INTEGER IRECVSTAT(MPI_STATUS_SIZE)
      DIMENSION WMSA(NMAX),SICP(NMAX),RPTS(3,NMAX)
      DIMENSION BUF1(NMAX),BUF2(NMAX),BUF3(3,NMAX),BUF0(3)
      DIMENSION BUF(MAX_OCC)
      DIMENSION MISTAKES(2)
C     COMMON/MIXPOT/POTIN(MAX_PTS*MXSPN),POT(MAX_PTS*MXSPN)
C     COMMON/TMP1/COULOMB(MAX_PTS),RHOG(MAX_PTS,10,MXSPN)
C     COMMON/TMP2/PSIG(NMAX,10,MAX_OCC)
C    &  ,PTS(NSPEED,3),GRAD(NSPEED,10,6,MAX_CON,3)
C    &  ,RVECA(3,MX_GRP),ICOUNT(MAX_CON,3)
!      COMMON/SICMAT/SIC(MAX_OCC,MAX_OCC,MXSPN)
!      COMMON/FRM/BFRM(3,MAX_OCC,MXSPN),RESULTS(13, MAX_OCC,MXSPN),
!     &   LFRM(MXSPN),DEBDAX(3,MAX_OCC,MXSPN)
       DATA MISTAKES/0,0/
       IF (SHMRANK.EQ.0) THEN
        PRINT *,'FATAL: PASLMSIC CALLED BY MANAGER'
        CALL STOPIT
       END IF

      IF (MODE .EQ. 1 ) THEN
       TAG=2401
       CALL MPI_RECV(ITRANS(1),3,MPI_INTEGER,
     &               0,TAG,SHMCOMM,IRECVSTAT,IERR)
       LSPN = ITRANS(1)
       LFN  = ITRANS(2)
       LFM  = LFN
       MPTS = ITRANS(3)
C      PRINT*,IRANK,'PAS RECV   LFM IS ONE :',LFM,LFN
       TAG=2402
       CALL MPI_RECV(WMSA(1),NMAX,MPI_DOUBLE_PRECISION,
     &               0,TAG,SHMCOMM,IRECVSTAT,IERR)
C      PRINT*,IRANK,'PAS RECV   LFM IS ZERO:',LFM
       TAG=2403
       CALL MPI_RECV(SICP(1),NMAX,MPI_DOUBLE_PRECISION,
     &               0,TAG,SHMCOMM,IRECVSTAT,IERR)
C      PRINT*,IRANK,'PAS RECV   LFM IS ZERO:',LFM
       TAG=2404
       CALL MPI_RECV(RPTS(1,1),3*NMAX,MPI_DOUBLE_PRECISION,
     &               0,TAG,SHMCOMM,IRECVSTAT,IERR)
C      PRINT*,IRANK,'PAS RECV   LFM IS ZERO:',LFM
       TAG=2405
!MGGA flag
       CALL MPI_RECV(ISMGGA,1,MPI_LOGICAL,
     &               0,TAG,SHMCOMM,IRECVSTAT,IERR)
!LIBXC flag
       TAG=2406
       CALL MPI_RECV(LIBXC1,1,MPI_LOGICAL,
     &               0,TAG,SHMCOMM,IRECVSTAT,IERR)
       IF(ISMGGA.OR.LIBXC1)THEN
        TAG=2407
        !allocate array for mgga
        if(.not.allocated(MIXINS)) then
         ALLOCATE(MIXINS(4,NMAX),STAT=IERR)
         IF(IERR/=0)WRITE(6,*)'ERROR ALLOCATING MIXINS'
        end if
        MIXINS(:,:)=0.0d0
        CALL MPI_RECV(MIXINS(1,1),4*NMAX,MPI_DOUBLE_PRECISION,
     &                0,TAG,SHMCOMM,IRECVSTAT,IERR)
       END IF
       CALL GTTIME(APT1)
       IF(LFM.EQ.0)THEN
C        PRINT*,IRANK,'PAS BEFORE LFM IS ZERO:',LFM,LFN
         LFM=MAX(LFN,LFM)
         MISTAKES(1)=MISTAKES(1)+1
         IF(MISTAKES(1).GT.MISTAKES(2))THEN
           MISTAKES(2)=MISTAKES(1)*2
           WRITE(6+IRANK,*)'LFM MISTAKES:',SHMRANK,MISTAKES
         END IF
       END IF
       CALL SICLMSLV(LSPN,LFM,MPTS,WMSA,SICP,RPTS)
C                 PRINT*,IRANK,'PAS AFTER  LFM IS ZERO:',LFM
       CALL GTTIME(APT2)
!       WRITE(6+IRANK,*)'I AM RANK:',SHMRANK,' AND I AM DONE',
!     &       APT2-APT1,MPTS
       TAG=1
       CALL MPI_SSEND(SHMRANK,1,MPI_INTEGER,0,TAG,SHMCOMM,IERR)
      END IF

      IF (MODE .EQ. 2 ) THEN
C        PRINT*,IRANK,' REDUCE',LFM,LSPN
       TAG=2501
!       CALL TRACER('REDUCING SIC')
!       CALL MPI_BARRIER(SHMCOMM,IERR)
       CALL MPI_REDUCE(SIC_COL(1),SIC_COL(1),MAX_OCC,
     &                  MPI_DOUBLE_PRECISION,MPI_SUM,0,
     &                  SHMCOMM,IERR)
!       DO JWF=1,MAX_OCC
!         SIC(JWF,LFM,LSPN)= SIC_COL(JWF)       
!       END DO
C      WRITE(6+IRANK,*)'REDUCE RETURNING RANK',IRANK
       !Deallocate an array for mgga
       IF(allocated(MIXINS))THEN
        DEALLOCATE(MIXINS,STAT=IERR)
        IF(IERR/=0)WRITE(6,*)'ERROR DEALLOCATING MIXINS'
       ENDIF
      END IF
#endif
      RETURN
      END
